print("Este programa realiza operações em sequencia")
numero = 10

soma=numero+5
multiplicacao=soma*2
subtracao=multiplicacao-8
divisao=subtracao/3
resto=divisao%4
potencia=resto**2

print(f"Após += 5: {soma} ")
print(f"Após *= 2: {multiplicacao}")
print(f"Após -= 8: {subtracao}")
print(f"Após /= 3: {divisao}")
print(f"Após %= 4: {resto}")
print(f"Após **= 2: {potencia}")